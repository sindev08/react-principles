/**
 * sync-registry.mjs
 *
 * Reads src/ui/*.tsx + shared utilities, transforms @/ imports,
 * and writes packages/cli/src/registry/templates.ts.
 *
 * Run before building the CLI:
 *   node scripts/sync-registry.mjs
 */

import { readFileSync, writeFileSync, readdirSync } from "fs";
import { join, basename } from "path";
import { fileURLToPath } from "url";

const __dirname = fileURLToPath(new URL(".", import.meta.url));
const ROOT = join(__dirname, "..");

const UI_DIR = join(ROOT, "src/ui");
const CN_SRC = join(ROOT, "src/shared/utils/cn.ts");
const HOOK_SRC = join(ROOT, "src/shared/hooks/useAnimatedMount.ts");
const OUT = join(ROOT, "packages/cli/src/registry/templates.ts");

function transform(src) {
  return src
    .replace(/from "@\/shared\/utils\/cn"/g, 'from "@/lib/utils"')
    .replace(/from "@\/shared\/hooks\/useAnimatedMount"/g, 'from "@/hooks/use-animated-mount"');
}

function escape(str) {
  return str.replace(/\\/g, "\\\\").replace(/`/g, "\\`").replace(/\$\{/g, "\\${");
}

const templates = {};

// utils
templates["utils"] = escape(readFileSync(CN_SRC, "utf8").trim());
templates["use-animated-mount"] = escape(readFileSync(HOOK_SRC, "utf8").trim());

// ui components
const files = readdirSync(UI_DIR).filter(
  (f) => f.endsWith(".tsx") && !f.includes(".stories.") && !f.includes(".test.")
);
for (const file of files) {
  const name = basename(file, ".tsx");
  const raw = readFileSync(join(UI_DIR, file), "utf8").trim();
  templates[name] = escape(transform(raw));
}

const lines = [
  "// AUTO-GENERATED by scripts/sync-registry.mjs — do not edit manually",
  "",
  "export const TEMPLATES: Record<string, string> = {",
  ...Object.entries(templates).map(([k, v]) => `  "${k}": \`${v}\`,`),
  "};",
  "",
];

writeFileSync(OUT, lines.join("\n"), "utf8");
console.log(`✓ Synced ${Object.keys(templates).length} templates → packages/cli/src/registry/templates.ts`);
